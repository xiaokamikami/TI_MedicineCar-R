/* USER CODE BEGIN 1 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if(htim==(&htim6))
    {
            SysTickCount++;
            if(SysTickCount>=5)//SysTick是1ms一次， 这里判断语句大于5就是5ms运行一次
            {

                SysTickCount=0;
                GetMotorPulse();                                 //获取电机A,B的编码脉冲  编码器
                BMotorPulse = abs(BMotorPulse );
                AMotorPulse = abs(AMotorPulse );
                MotorCouter1=BMotorPulse+MotorCouter1;
                MotorCouter2=AMotorPulse+MotorCouter2;
            }

            else if(SysTickCount==3)
            {

                //printf("BM:%d,AM:%d,SP:%d,",BMotorPulse,AMotorPulse,SpeedTarget);
                MotorOutput1 = SpeedInnerControl1(BMotorPulse,SpeedTarget);       //PID
                MotorOutput2 = SpeedInnerControl2(AMotorPulse,SpeedTarget);       //PID

                //printf("M:%d,",MotorOutput);
            }

            else if(SysTickCount==2)//电机按键开关
            {
                switch (MovingDirection)
                {
                    case 1:
                        VoltageAndDirection(MotorOutput1,MotorOutput2);               //电机输出 前进
                        break;
                    case 2:
                        OpposeVoltageAndDirection(MotorOutput1,MotorOutput2);         //电机输出 后退
                        break;
                    case 3:
                        LeftDifferential(MotorOutput1,MotorOutput2);         //左差数
                        break;
                    case 4:
                        RightDifferential(MotorOutput1,MotorOutput2);        //右差数
                        break;
                }
            }

    }
}

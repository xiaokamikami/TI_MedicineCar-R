/**
  * @brief 串口中断回调函数		中断接收串口2的数据
  * @param 调用回调函数的串口
  * @note  串口每次收到数据以后都会关闭中断，如需重复使用，必须再次开启
  * @retval None
  */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if(huart->Instance==USART2)
    {
        UART2_Rx_Buf[UART2_Rx_cnt] = UART2_temp[0];
        UART2_Rx_cnt++;
        if(0x0a == UART2_temp[0])
        {
            UART2_Rx_flg = 1;
        }

    }
}
